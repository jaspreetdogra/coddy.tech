# Cast Mastery - Division of Two Float Numbers

# Challenge:
# Beginner
# Write a program that takes two inputs from the user.
# These inputs should be treated as numbers with decimal points.
#
# Task:
# - Store the inputs in two variables
# - Convert them to float type
# - Print the result of dividing the first number by the second number
# Note: Each test case provides exactly two inputs.

# Read first number and convert to float
num1 = float(input())

# Read second number and convert to float
num2 = float(input())

# Divide the first number by the second
result = num1 / num2

# Print the result
print(result)
