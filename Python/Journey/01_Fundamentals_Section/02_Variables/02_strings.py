# Lesson 2: Strings
#
# A char is a single character (e.g., 1, 6, %, b, p, ., T).
# A string (str) consists of multiple characters.
#
# Strings are written inside single ('') or double ("") quotes.
#
# Examples:
s1 = 'This is a string'
s2 = "This is also a string"
