# Arithmetic Operators

# Operators are used to perform operations on values. 
# First we will discuss the most basic arithmetic operators, 
# they may be familiar from math classes.

# Operator      Operation       Example
# +             Addition        3 + 2 = 5
# -             Subtraction     3 - 2 = 1
# *             Multiplication  3 * 2 = 6
# /             Division        4 / 2 = 2

# Let's see a usage example.
a = 3
b = 5
c = a + b  # c holds 8
print(f"a = {a}, b = {b}, c = {c}")

