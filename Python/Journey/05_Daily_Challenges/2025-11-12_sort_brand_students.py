def sort_students(students):
    """
    Sorts a list of student names alphabetically.
    
    Parameters:
        students (list): List of student names as strings.
        
    Returns:
        list: Student names sorted in alphabetical order.
    """
    # Step 1: Sort the list using Python's built-in sorted() function
    sorted_list = sorted(students)
    
    # Step 2: Return the sorted list
    return sorted_list
